#include "3-calc.h"

/**
 * op_add - return the sum of 2 numbers
 * @a: number one
 * @b: number two
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - return the sub of two numbers
 * @a: number one
 * @b: number two
 * Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - return mul of two numbers
 * @a: number one
 * @b: number two
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers
 * @a: number one
 * @b: number two
 * Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return modulo of two numbers
 * @a: number one
 * @b: number two
 * Return a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
